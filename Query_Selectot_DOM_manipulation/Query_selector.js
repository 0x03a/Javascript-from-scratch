 /*
  <div> (Division Tag)
Purpose: The <div> tag is a block-level container element used to group content together. It doesn’t have any semantic meaning by itself but is commonly used for layout purposes in combination with CSS and JavaScript.

Characteristics:

Block-level element (takes up the full width available).
Commonly used to create sections or containers in a webpage.

Code:
<div style="background-color: lightblue; padding: 20px;">
    <h2>This is a section inside a div</h2>
    <p>The div tag is a block-level container for grouping elements.</p>
</div>

Output: The content will be wrapped 
in a block with a light blue background and some padding around it.


 */



/*

<span> (Span Tag)
Purpose: The <span> tag is an inline container used to group inline elements or apply styles without disrupting the flow of the document. It’s similar to <div>, but it is inline rather than block-level.

Characteristics:

Inline element (does not take up the full width).
Commonly used to apply styling to a small portion of text or content within a larger block of text.

Code:
<p>This is a <span style="color: red;">highlighted</span> word in a sentence.</p>

Output: Only the word "highlighted" will be styled with a red color,
 and it will remain inline with the rest of the text.

*/




/*

<nav> (Navigation Tag)
Purpose: The <nav> tag is a semantic block-level element used to define a section of navigation links. It provides meaning to search engines and screen readers by identifying that the content inside is for navigation.

Characteristics:

Block-level element.
Used to group navigation links such as menus, sidebars, or footer links.

Code:
<nav>
    <ul>
        <li><a href="#home">Home</a></li>
        <li><a href="#about">About</a></li>
        <li><a href="#contact">Contact</a></li>
    </ul>
</nav>


Output: A list of navigation links styled as a block-level element

*/



/*



<pre> (Preformatted Text Tag)
Purpose: The <pre> tag is used to display preformatted text, preserving spaces, line breaks, and formatting exactly as it is written in the HTML. It is typically used for displaying code or formatted text where spacing matters.

Characteristics:

Block-level element.
Displays text exactly as it is written, including spaces and line breaks.

Code:

<pre>
    Line 1
        Line 2 (Indented)
    Line 3
</pre>

Output: The text will be displayed with line breaks and spaces exactly as shown in the HTML source.




*/


/*

<code> (Code Tag)
Purpose: The <code> tag is used to display inline snippets of code. It doesn’t preserve formatting (like <pre> does) but is often used to semantically indicate code within a paragraph.

Characteristics:

Inline element.
Commonly used for short code snippets within text.
Often displayed in a monospace font by default.

Code:

<p>Here is a simple Python code: <code>print("Hello, World!")</code></p>

Output: The print("Hello, World!") part will be displayed in a monospace font, visually indicating that it is code.

*/



